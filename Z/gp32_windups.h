u8 transChar(u8 l) {  // Si el caractes es >175 le buscamos la correspondencia debajo de 175

    char cesp[] ="-----------------";// 46
    char cesp2[]="";
    char aeiou[]="AEIOU";

    u16 i;

    // Carcteres especiales 2
    if(l==170) l=166;
    if(l==171) l=174;
    if(l==176) l=167;
    if(l==186) l=167;
    if(l==187) l=175;
    
    
    // Carcteres especiales 2
    if(l>175) {
        for(i=0;i<46;i++)
            if(cesp[i]==l) {
                l=128+i;
                break;
            }
    } 

    // Carcteres especiales 3
    if(l>175) {
        for(i=0;i<46;i++)
            if(cesp2[i]==l) {
                l=aeiou[i%5];
                break;
            }
    } 

    return l;
}


int ancho_letra(char l) {

    l=transChar(l);

    if(l<176)
        return anchos[l];
    else
        return 0;
}

void textOut(char *cad,u16 x,u16 y,u8 color,u8 bold) {
    int i;                   int flash=0, bar=1; 
    u16 j,k,l,c;
    u8  linea=3;
    u8  anchoLetra;
    u16 desp;            // Es el nmero de pixels que nos vamos desplazando por la frase
                         // dependiendo del ancho de cada letra
    static int avoidshadow=0;

    desp=0;
    for(k=0;cad[k]!='\0';k++) {
        int fix=0; //rlyeh
//        if(cad[k]=='>') fix=-1; //3,2 corto izq, 4,1 desp der 1 pix
        if(cad[k]=='\n') { y+=8+3; desp=0; } //rlyeh

        l=transChar(cad[k]);

        if(l==0xd || l<32) {
            continue;
        } 

        anchoLetra=anchos[l];

        linea=(l-32)/32;
        l=l%32;

        if(cad[k]=='^') { /*if((avoidshadow++)%4<2)*/ flash^=1; continue; }
        if(cad[k]=='#') { while(cad[++k]!='#'); if(progressnum) { progress[bar].x=x+desp; progress[bar].y=y+6; bar++; } desp+=100+3; continue; }

        if(flash)
        {
         for(i=0;i<anchoLetra+1;i++) 
             for(j=0;j<12;j++) *(Picture+(x+i+desp  )*240+240 -j-y-1)=BLACK_COLOR;
        }

        for(i=fix;i<anchoLetra+fix;i++) {
            for(j=0;j<12;j++) {
                //*(lcd+(x+i+k*12)*60+j+y)=*(fontA11+(i+l*11+1)*15+j-linea);
                //c=*(arial11+(i+l*11+1)*60 +11-j-linea*3*4);
                c=*(arial11+(linea*12+j)*352+l*11+i);
                if(c==0) {
                    *(Picture+(x+i+desp  )*240+240 -j-y-1)=color;
                    if(bold)
                        *(Picture+(x+i+desp+1)*240+240 -j-y-1)=color; // Para hacer negrita
                }
            }
        }
        if (bold)
            desp+=anchoLetra+2;
        else
            desp+=anchoLetra+1;

    }
}

